# Take 3 input from user and perform sum, sub, mul, div
num1 = int(input("Enter the num 1"))
num2 = int(input("Enter the num 2"))
num3 = int(input("Enter the num 3"))
Sum = num1 + num2 + num3
Sub = num1 - num2 - num3
Mul = num1 * num2 * num3
Div = num1 / num2 / num3
print("Sum is -> ", Sum)
print("Sub is -> ", Sub)
print("Mul is -> ", Mul)
print("Div is -> ", Div)
